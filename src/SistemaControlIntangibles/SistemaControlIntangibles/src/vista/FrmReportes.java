    private void buscarIntangibleporID() {
        try {
            int id = Integer.parseInt(txtIdBuscar.getText());

            // Llamar al DAO para buscar el intangible por ID
            IntangibleDAO dao = new IntangibleDAO();
            ArrayList<Intangible> lista = dao.listarIntangibles();
            Intangible encontrado = null;

            for (Intangible i : lista) {
                if (i.getId() == id) {
                    encontrado = i;
                    break;
                }
            }
            if (encontrado != null) {
                //Formatear la fecha correcta
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                String fechaFormateada = encontrado.getFechaAdquisicion() 
                        != null ? sdf.format(encontrado.getFechaAdquisicion()) 
                        : "No disponible";
                // Mostrar los datos
                lblNombre.setText("Nombre del Tutor: " + encontrado.getNombre());
                lblTipo.setText("Tipo Licencia: " + encontrado.getTipoLicencia());
                lblFecha.setText("Fecha Adquisición: " + fechaFormateada);
                lblVida.setText("Vida útil: " + encontrado.getVidaUtil() + " Años" );
                lblCosto.setText("Costo: $" + encontrado.getCosto());
                lblAnual.setText("Amortización anual: $" + encontrado.getAmortizacionAnual());
                lblMensual.setText("Amortización mensual: $" + encontrado.getAmortizacionMensual());
                lblLibros.setText("Valor en libros: $" + encontrado.getValorLibros());
                lblRemanente.setText("Remanente: $" + encontrado.getRemanente());
                lblCuotas.setText("Cuotas totales: " + encontrado.getCuotasTotal());
                lblCuotasAm.setText("Cuotas amortizadas: " + encontrado.getCuotasAmortizadas());    
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró ningún intangible con ID: " + id);
            }
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingresa un número válido");
        }
    }
