    private void mostrarTotales(){
        IntangibleDAO dao = new IntangibleDAO();
        ArrayList<Intangible> lista = dao.listarIntangibles();
        
        int totalIntangibles = lista.size();
        double sumaCostos = 0;
        double sumaRemanentes = 0;
        int sumaVidaUtil = 0;
        
        for(Intangible i : lista){
            sumaCostos += i.getCosto();
            sumaRemanentes += i.getRemanente();
            sumaVidaUtil += i.getVidaUtil();
        }
        
        double promedioVida = (totalIntangibles > 0) ? 
                (double) sumaVidaUtil / totalIntangibles : 0;
        
        //Mostrar resultados en las etiquetas
        lblTotalIntangiblesValor.setText(String.valueOf(totalIntangibles));
        lblSumaCostosValor.setText(String.format("%.2f", sumaCostos));
        lblPromVidaValor.setText(String.format("%.2f", promedioVida));
        lblTotalRemanenteValor.setText(String.format("%.2f", sumaRemanentes));
    }

