    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        //String nombre = txtNombre.getText();
        String usuario = txtUsuario.getText();
        String contra = new String(txtContrasena.getPassword());
        
        if(/*nombre.isEmpty() ||*/ usuario.isEmpty() || contra.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }
        
        Connection con = Conexion.getConexion();
        PreparedStatement ps;
        
        try{
            String sql = "INSERT INTO usuarios (usuario, contrasena) VALUES (?, ?)";
            ps = con.prepareStatement(sql);
            //ps.setString(1, nombre);//
            ps.setString(1, usuario);
            ps.setString(2, contra);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Usuario Registrado correctamente");
            
            ps.close();
            con.close();
            
            //Cerrar ventana o limpiar campos
            this.dispose();  //Si quiere cerrar el formulario
            // o limpiar:
            // txtNombre.setText(""); txtUsuario.setText(""); txtContrasena.setText("");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error al registrar usuario: " +
            e.getMessage());
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

